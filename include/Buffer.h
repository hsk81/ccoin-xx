struct buffer {
	void *pointer;
	size_t length;
};

struct const_buffer {
    const void *pointer;
    size_t length;
};

namespace Buffer {

    guint g_hash(gconstpointer key);
    gboolean g_equal(gconstpointer lhs, gconstpointer rhs);
    void free(struct buffer *buffer);
    void g_free(gpointer data);
    struct buffer *copy(const void *data, size_t length);
}

